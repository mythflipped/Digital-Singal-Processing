w=-pi:0.01:pi;
z = exp(1j*w);
r1 = 0.99;r2 = 0.9; r3 = 0.7;
H1 = (1-r1)./(1-2*r1*cos(pi/3).*z.^(-1)+(r1.^2)*(z.^(-2)));
H2 = (1-r2)./(1-2*r2*cos(pi/3).*z.^(-1)+(r2.^2)*(z.^(-2)));
H3 = (1-r3)./(1-2*r3*cos(pi/3).*z.^(-1)+(r3.^2)*(z.^(-2)));
figure(1)
subplot(3,1,1);
plot(w,abs(H1));
title('r=0.99 江鹏展 11811003');
subplot(3,1,2);
plot(w,abs(H2));
title('r=0.9 江鹏展 11811003');
subplot(3,1,3);
plot(w,abs(H3));
title('r=0.7 江鹏展 11811003');